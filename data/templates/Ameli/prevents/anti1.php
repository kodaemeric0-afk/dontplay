if (!function_exists('_ab_wildcard_to_regex')) {
    function _ab_wildcard_to_regex($pattern) {
        // Patterns already starting with ^ are treated as partial regex (legacy format)
        if (strpos($pattern, '^') === 0) {
            // Replace wildcard .* used in legacy patterns — already valid regex
            return '/' . str_replace(' ', '', $pattern) . '/';
        }
        // Plain IP — exact match
        return '/^' . preg_quote($pattern, '/') . '$/';
    }
}

if (!function_exists('_ab_ip_blocked')) {
    function _ab_ip_blocked($ip, $blockedIps) {
        foreach ($blockedIps as $entry) {
            $entry = trim($entry);
            if ($entry === '') continue;
            $regex = _ab_wildcard_to_regex($entry);
            if (@preg_match($regex, $ip)) {
                return true;
            }
        }
        return false;
    }
}

